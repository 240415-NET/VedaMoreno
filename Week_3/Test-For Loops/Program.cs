namespace Test_For_Loops;

class Program
{
    static void Main(string[] args)
    {
        // Example of for loop
        for (int i = 0; i < 10; i++)
        {
            Console.WriteLine("Value of i: {0}", i);
        }



        // Example of REVERSE for loop
        for (int i = 0; i < 10; i++)
        {
            Console.WriteLine("Value of i: {0}", i);
        }
    }

}

